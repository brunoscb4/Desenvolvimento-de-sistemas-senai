package Rivisao;

public class EX_FOR_25 {
    public static void main(String[] args) {
        int altura =5;
        for (int i = 1; i <=altura ; i++) {
            System.out.printf(" ");
            for (int j = 1; j <=i ; j++) {
                System.out.print("*");

            }
            System.out.println();
        }
    }
}
